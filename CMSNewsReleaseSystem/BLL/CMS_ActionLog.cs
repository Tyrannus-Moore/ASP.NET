		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Maticsoft.Model.CMS_ActionLog> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.CMS_ActionLog> modelList = new List<Maticsoft.Model.CMS_ActionLog>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.CMS_ActionLog model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new Maticsoft.Model.CMS_ActionLog();
					if(dt.Rows[n]["Id"].ToString()!="")
					{
						model.Id=int.Parse(dt.Rows[n]["Id"].ToString());
					}
					model.Name=dt.Rows[n]["Name"].ToString();
					model.Description=dt.Rows[n]["Description"].ToString();
					model.Time=dt.Rows[n]["Time"].ToString();
					model.Type=dt.Rows[n]["Type"].ToString();
					modelList.Add(model);
				}
			}
			return modelList;
		}

